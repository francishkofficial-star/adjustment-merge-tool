    name = row['商品名称']
    budget_cat = csp_to_budget.get(name, '未分类')
    # 查找预算模板行号
    budget_row = -1
    for i in range(2, len(budget)):
        if budget.iloc[i, 2] == budget_cat:
            budget_row = i
            break
    print(f"  预算行{budget_row}: {code} | {name} | 类目={budget_cat}")
